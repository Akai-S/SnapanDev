<?php
// operator untuk operasi aritmatika, Digunakan untuk menghitung angka, seperti penjumlahan, pengurangan, perkalian, dan pembagian.

$x = 7;
$y = 3;

// ------------------------------------------------------------
// operator aritmatika ada :
// "+" -> penjumlahan
// "-" -> pengurangan
// "*" -> perkalian
// "/" -> pembagian
// ------------------------------------------------------------

echo $x - $y; // hasilnya 4
echo "<br>";
echo $x * $y; // hasilnya 21
echo "<br>";

// ------------------------------------------------------------

$p = 20;
$q = 4;
$hasilBagi = $p / $q; // hasilnya 5

echo "Hasil pembagian $p dengan $q adalah $hasilBagi.";
?>
